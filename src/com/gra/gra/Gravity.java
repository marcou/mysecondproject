package com.gra.gra;

public class Gravity {
	private float x_power;
	private float y_power;
	
	public Gravity(float x_power, float y_power){
		this.x_power = x_power;
		this.y_power = y_power;
	}

	public float getX_power() {
		return x_power;
	}

	public void setX_power(float x_power) {
		this.x_power = x_power;
	}

	public float getY_power() {
		return y_power;
	}

	public void setY_power(float y_power) {
		this.y_power = y_power;
	}


	
}